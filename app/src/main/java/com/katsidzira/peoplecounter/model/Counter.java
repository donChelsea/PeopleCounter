package com.katsidzira.peoplecounter.model;

public class Counter {

    private int total;
    private int people;

    public Counter() {
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getPeople() {
        return people;
    }

    public void setPeople(int people) {
        this.people = people;
    }
}
